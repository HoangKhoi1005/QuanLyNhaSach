    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        if (txtMaNV.getText().isEmpty() || txtTenNV.getText().isEmpty() || txtSoDT.getText().isEmpty() || txtEmail.getText().isEmpty() || txtDiaChiNV.getText().isEmpty() || cboChucVu.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin sách.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String email = txtEmail.getText();
        if (!isValidEmail(email)) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập địa chỉ email hợp lệ.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }


        // Tiến hành thêm sách vào cơ sở dữ liệu với các thông tin đã kiểm tra
        NhanVienDTO nv = new NhanVienDTO();
        nv.setMaNV(txtMaNV.getText());
        nv.setHoTenNV(txtTenNV.getText());
        nv.setDiaChiNV(txtDiaChiNV.getText());
        nv.setEmailNV(txtEmail.getText());
        nv.setSoDT(txtSoDT.getText());
        ChucVuDTO cv = (ChucVuDTO) cboChucVu.getSelectedItem();
        nv.setMaCV(cv.getMaCV());
        nv.setAnhNV(duongDanAnh);
        nv.setTrangThai(1);
        boolean kq = NhanVienDAO.capNhatNhanVien(nv);
        if(kq)
        {
            JOptionPane.showMessageDialog(this, "Nhân viên đã được cập nhật thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            ArrayList<NhanVienDTO> ds = NhanVienDAO.layDS();
            formQuanLyNhanVien.capNhatDuLieuBang(ds);
            //            SwingUtilities.getWindowAncestor(this).dispose();
        }
        else
        JOptionPane.showMessageDialog(this, "Cập nhật nhân viên thất bại.", "Thông báo", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnCapNhatActionPerformed

