    private void loadCboCV()
    {  
        loadChucVu load = new loadChucVu();
        List<ChucVuDTO> list = load.getData();
        cboChucVu.removeAllItems();
        list.forEach(o -> {
            cboChucVu.addItem(o);
        });
    }
    
