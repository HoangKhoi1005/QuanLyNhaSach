    private void btnSuaKMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaKMActionPerformed

        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        try {
            Date date1 = dateFormat.parse(txtNgayBD.getText());
            Date date2 = dateFormat.parse(txtNgayKT.getText());

            if (date2.after(date1)) {
                KhuyenMaiDTO kh = new KhuyenMaiDTO();
                kh.setMaKM(txtMaKM.getText());
                kh.setTenKM(txtTenKhuyenMai.getText());
                Date ngayBD = dateFormat.parse(txtNgayBD.getText());
                kh.setNgayBD(ngayBD);
                Date ngayKT = dateFormat.parse(txtNgayKT.getText());
                kh.setNgayKT(ngayKT);
                kh.setGiamGia(Double.parseDouble(txtGiamGia.getText()));
                boolean kq = KhuyenMaiDAO.capNhatKhuyenMai(kh);
                if(kq)
                {
                    JOptionPane.showMessageDialog(this, "Cập nhật khuyến mãi thành công");
                    loadtblKhuyenMai();
                }
                else
                    JOptionPane.showMessageDialog(this, "Cập nhật khuyến mãi thất bại");
                
            } else {
                JOptionPane.showMessageDialog(this, "Ngày bắt đầu lớn hơn ngày kết thúc!");
            }
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Định dạng không hợp lệ");
        }
    }//GEN-LAST:event_btnSuaKMActionPerformed

