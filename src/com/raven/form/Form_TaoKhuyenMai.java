    public void displaySelectedRow() {
        int selectedRow = tblKhuyenMai.getSelectedRow();
        if (selectedRow != -1) {
            // Lấy giá trị từ các cột của dòng được chọn
            String maKM = (String) tblKhuyenMai.getValueAt(selectedRow, 0);
            String tenKM = (String) tblKhuyenMai.getValueAt(selectedRow, 1);
            String ngayBD = (String) tblKhuyenMai.getValueAt(selectedRow, 2);
            String ngayKT = (String) tblKhuyenMai.getValueAt(selectedRow, 3);
            double khuyenMai = Double.parseDouble(tblKhuyenMai.getValueAt(selectedRow, 4).toString());
            
            txtMaKM.setText(maKM);
            txtTenKhuyenMai.setText(tenKM);
            txtNgayBD.setText(ngayBD);
            txtNgayKT.setText(ngayKT);
            txtGiamGia.setText(String.valueOf(khuyenMai));
        }
    }
    public void loadtblKhuyenMai()
    {
        conn.open();
        String sql = "Select * FROM KHUYENMAI";
        ResultSet rs = conn.executeQuery(sql);
        DefaultTableModel model = (DefaultTableModel) this.tblKhuyenMai.getModel();
        model.setRowCount(0);
        try {
            while(rs.next()) {
                String maKM = String.valueOf(rs.getString("MAKM"));
                String tenKM = String.valueOf(rs.getString("TENKM"));
                String ngayBD = String.valueOf(rs.getString("NGAYBD"));
                String ngayKT = String.valueOf(rs.getString("NGAYKT"));
                double khuyenMai = Double.parseDouble(rs.getString("GIAMGIA"));
                Object[] rowData = {maKM,tenKM,ngayBD,ngayKT,khuyenMai};
                    model.addRow(rowData);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        conn.close();
    }

