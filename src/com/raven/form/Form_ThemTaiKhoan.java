     private void loadCboNV() {  
        NhanVienDAO nhanVienDAO = new NhanVienDAO();
        List<NhanVienDTO> allNhanVien = nhanVienDAO.layDS();
        List<TaiKhoanDTO> allTaiKhoan = TaiKhoanDAO.layDS();

        List<String> maNVsWithAccount = allTaiKhoan.stream()
                                                   .map(TaiKhoanDTO::getMaNV)
                                                   .collect(Collectors.toList());

        List<NhanVienDTO> nhanVienWithoutAccount = allNhanVien.stream()
                                                              .filter(nv -> !maNVsWithAccount.contains(nv.getMaNV()))
                                                              .collect(Collectors.toList());

        cboNhanVien.removeAllItems();
        nhanVienWithoutAccount.forEach(nv -> {
            cboNhanVien.addItem(nv);
        });
    }
