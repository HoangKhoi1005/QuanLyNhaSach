    private void btnThemTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemTKActionPerformed
        String tenDN = txtTenDN.getText();
        String matKhau = new String(txtMatKhau.getPassword());
        String tenHT = txtTenHT.getText();
        String phanQuyen = (String) cboPhanQuyen.getSelectedItem();
        NhanVienDTO nv = (NhanVienDTO) cboNhanVien.getSelectedItem();
        String maNV = nv.getMaNV();
        if (tenDN.isEmpty() || matKhau.isEmpty() || tenHT.isEmpty() || phanQuyen == null || maNV == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        TaiKhoanDTO taiKhoan = new TaiKhoanDTO();
        taiKhoan.setTenDN(tenDN);
        taiKhoan.setTenHT(tenHT);
        taiKhoan.setMatKhau(matKhau);
        taiKhoan.setPhanQuyen(phanQuyen);
        taiKhoan.setMaNV(maNV);

        boolean kq = TaiKhoanDAO.themTaiKhoan(taiKhoan);
        if (kq) {
            JOptionPane.showMessageDialog(this, "Tài khoản đã được thêm thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            ArrayList<TaiKhoanDTO> ds = TaiKhoanDAO.layDS();
            formQuanLytaiKhoan.capNhatDuLieuBang(ds);
        } else {
            JOptionPane.showMessageDialog(this, "Thêm tài khoản thất bại.", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnThemTKActionPerformed


