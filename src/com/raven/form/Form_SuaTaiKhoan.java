    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        int selectedRow = formQuanLyTaiKhoan.getSelectedRowIndex();
        if (selectedRow != -1) {
            if (txtTenHT.getText().isEmpty() || txtTenDN.getText().isEmpty() || txtMatKhau.getText().isEmpty() || cboPhanQuyen.getSelectedItem() == null || cboNhanVien.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin tài khoản.", "Thông báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            TaiKhoanDTO taiKhoan = new TaiKhoanDTO();
            taiKhoan.setTenHT(txtTenHT.getText());
            taiKhoan.setTenDN(txtTenDN.getText());
            taiKhoan.setMatKhau(txtMatKhau.getText());
            taiKhoan.setPhanQuyen(cboPhanQuyen.getSelectedItem().toString());
            NhanVienDTO nhanVien = (NhanVienDTO) cboNhanVien.getSelectedItem();
            taiKhoan.setMaNV(nhanVien.getMaNV());

            boolean kq = TaiKhoanDAO.capNhatTaiKhoan(taiKhoan);
            if (kq) {
                JOptionPane.showMessageDialog(this, "Tài khoản đã được cập nhật thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                ArrayList<TaiKhoanDTO> ds = TaiKhoanDAO.layDS();
                formQuanLyTaiKhoan.capNhatDuLieuBang(ds);
                SwingUtilities.getWindowAncestor(this).dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật tài khoản thất bại.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng muốn cập nhật.", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

