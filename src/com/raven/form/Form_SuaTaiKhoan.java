    private void loadCboPhanQuyen() {
        cboPhanQuyen.removeAllItems();
        // Assuming roles are defined in an array or can be fetched from a data source
        String[] roles = {"Admin", "User"}; 
        for (String role : roles) {
            cboPhanQuyen.addItem(role);
        }
    }
    private void selectNhanVien(String tenNV) {
        for (int i = 0; i < cboNhanVien.getItemCount(); i++) {
            NhanVienDTO nhanVien = (NhanVienDTO) cboNhanVien.getItemAt(i);
            if (nhanVien.getHoTenNV().equals(tenNV)) {
                cboNhanVien.setSelectedIndex(i);
                break;
            }
        }
    }
    private void loadCboNV() {  
        NhanVienDAO nhanVienDAO = new NhanVienDAO();
        List<NhanVienDTO> allNhanVien = nhanVienDAO.layDS();
        List<TaiKhoanDTO> allTaiKhoan = TaiKhoanDAO.layDS();

        List<String> maNVsWithAccount = allTaiKhoan.stream()
                                                   .map(TaiKhoanDTO::getMaNV)
                                                   .collect(Collectors.toList());

        List<NhanVienDTO> nhanVienWithoutAccount = allNhanVien.stream()
                                                              .filter(nv -> !maNVsWithAccount.contains(nv.getMaNV()))
                                                              .collect(Collectors.toList());

        cboNhanVien.removeAllItems();
        nhanVienWithoutAccount.forEach(nv -> {
            cboNhanVien.addItem(nv);
        });
    }
