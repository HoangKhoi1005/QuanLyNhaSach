    public void updateTableDataChiTietPN() {
        model = (DefaultTableModel) tblSachNhap.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ trước khi cập nhật mới
        SachDTO sach = (SachDTO) cboMaSach.getSelectedItem();
        String sql = "Select MAPHIEUNHAP,CHITIETPN.MASACH,TENNXB,TENTL,TENSACH,TENTG,NGAYXUATBAN,GIANHAP,SOLUONGNHAP from SACH,NHAXUATBAN,THELOAI,TACGIA,CHITIETPN where SACH.MANXB=NHAXUATBAN.MANXB and SACH.MATL=THELOAI.MATL and SACH.MATG=TACGIA.MATG and SACH.MASACH=CHITIETPN.MASACH and MAPHIEUNHAP = '"+txtMaPhieuNhap.getText()+"'";
        conn.open();
        ResultSet rs = conn.executeQuery(sql);
        try {
            while(rs.next()) {
                String maPhieuNhap = rs.getString("MAPHIEUNHAP");
                String maSach = rs.getString("MASACH");
                String tenNXB = rs.getNString("TENNXB");
                String tenTL = rs.getNString("TENTL");
                String tenSach = rs.getNString("TENSACH");
                String tenTG = rs.getNString("TENTG");
                String ngayXB = rs.getDate("NGAYXUATBAN").toString();
                Double giaNhap1 = rs.getDouble("GIANHAP");
                int soLuongNhap = rs.getInt("SOLUONGNHAP");
                Object[] rowData = {maPhieuNhap, maSach,tenNXB, tenTL, tenSach, tenTG, ngayXB, giaNhap1,soLuongNhap};
                model.addRow(rowData);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
