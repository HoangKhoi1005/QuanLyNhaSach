    private void btnThemSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSachActionPerformed
        conn.open();

        if(txtGiaNhap.getText().isEmpty()) {
            showMessage("Vui lòng nhập giá nhập!");
            conn.close();
            return;
        }

        Double giaNhap = null;
        try {
            giaNhap = Double.parseDouble(txtGiaNhap.getText());
        } catch (NumberFormatException e) {
            showMessage("Giá nhập phải là một số hợp lệ!");
            conn.close();
            return;
        }

        if(txtSoLuong.getText().isEmpty()) {
            showMessage("Vui lòng nhập số lượng!");
            conn.close();
            return;
        }

        int soLuong = 0;
        try {
            soLuong = Integer.parseInt(txtSoLuong.getText());
        } catch (NumberFormatException e) {
            showMessage("Số lượng phải là một số hợp lệ!");
            conn.close();
            return;
        }

        if(txtMaPhieuNhap.getText().isEmpty()) {
            showMessage("Vui lòng tạo phiếu nhập trước khi thêm sản phẩm!");
            conn.close();
            return;
        }

        Double tongtien = giaNhap * soLuong;
        ChiTietPhieuNhapDTO ctpn = new ChiTietPhieuNhapDTO();
        ctpn.setMaPhieuNhap(txtMaPhieuNhap.getText());
        SachDTO sach = (SachDTO) cboMaSach.getSelectedItem();
        if (sach == null) {
            showMessage("Vui lòng chọn sách hợp lệ!");
            conn.close();
            return;
        }
        ctpn.setMaSach(sach.getMaSach());
        ctpn.setGiaNhap(giaNhap);
        ctpn.setSoLuongNhap(soLuong);
        ctpn.setTongTien(tongtien);

        if (ChiTietPhieuNhapDAO.kiemTraTrungKhoaChinh(ctpn.getMaSach(), ctpn.getMaPhieuNhap())) {
            JOptionPane.showMessageDialog(this, "Sách đã tồn tại trong phiếu: " + ctpn.getMaSach() + ", " + ctpn.getMaPhieuNhap());
            conn.close();
            return;
        }

        boolean kq = ChiTietPhieuNhapDAO.themChiTietPN(ctpn);
        tongTienPN();
        String sql = "UPDATE SACH SET SOLUONGTON = SOLUONGTON + ? WHERE MASACH = ?";
        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setInt(1, soLuong);
            ps.setString(2, sach.getMaSach());
            ps.executeUpdate();
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi cập nhật số lượng sách: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            conn.close();
            return;
        }

        if (kq) {
            JOptionPane.showMessageDialog(this, "Sách đã được thêm thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            updateTableDataChiTietPN();
            updateTableDataPhieuNhap();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm Sách thất bại.", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
        conn.close();
    }//GEN-LAST:event_btnThemSachActionPerformed

