    private boolean showMessage(String message) {
    Frame parentFrame = getParentFrame(this);
    if (parentFrame != null) {
        Message obj = new Message(parentFrame, true);
        obj.showMessage(message);
        return obj.isOk();
    } else {
        return false;
    }
}

private Frame getParentFrame(Component component) {
    while (component != null) {
        if (component instanceof Frame) {
            return (Frame) component;
        }
        component = component.getParent();
    }
    return null;
}

    
    public void tongTienPN() {
        String sql = "SELECT SUM(TONGTIEN) AS tongTien FROM CHITIETPN WHERE MAPHIEUNHAP = ?";
        int thanhTien = 0;
        try (PreparedStatement statement = conn.prepareStatement(sql)) {
            statement.setString(1, txtMaPhieuNhap.getText());
            ResultSet rs = statement.executeQuery();

            if (rs.next()) {
                thanhTien = rs.getInt("tongTien");
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

        // Update THANHTIEN in PHIEUNHAP table
        sql = "UPDATE PHIEUNHAP SET THANHTIEN = ? WHERE MAPHIEUNHAP = ?";
        try (PreparedStatement statement = conn.prepareStatement(sql)) {
            statement.setInt(1, thanhTien);
            statement.setString(2, txtMaPhieuNhap.getText());
            statement.executeUpdate();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

        txtTongCong.setText(String.valueOf(thanhTien));
    }


