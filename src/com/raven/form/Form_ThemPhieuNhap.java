    private void btnTaoPhieuNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoPhieuNhapActionPerformed
        conn.open();
        LocalDate homNay = LocalDate.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String strDay = homNay.format(formatter);

        String sql = "SELECT COUNT(*) AS count FROM PHIEUNHAP WHERE NGAYNHAP = '" + strDay + "'";
        ResultSet rs = conn.executeQuery(sql);
        try {
            if (rs.next()) {
                int stt = rs.getInt("count");
                stt++;
                String strStt = String.format("%03d", stt);
                String strMa = "PN" + homNay.format(DateTimeFormatter.ofPattern("ddMMyyyy")) + strStt;
                txtMaPhieuNhap.setText(strMa);
                PhieuNhapDTO pn = new PhieuNhapDTO();
                pn.setMaPhieuNhap(strMa);
                LocalDate ngayNhapLocalDate = LocalDate.parse(strDay);
                Date ngayNhapDate = java.sql.Date.valueOf(ngayNhapLocalDate);
                pn.setNgayNhap(ngayNhapDate);
                NhanVienDTO nv = (NhanVienDTO) cboNhanVien.getSelectedItem();
                pn.setMaNV(nv.getMaNV());
                NhaCungCapDTO ncc = (NhaCungCapDTO) cboNhaCungCap.getSelectedItem();
                pn.setMaNCC(ncc.getMaNCC());
                pn.setThanhTien(0.0);
                boolean kq = PhieuNhapDAO.themPhieuNhap(pn);
                if(kq)
                {
                    JOptionPane.showMessageDialog(this, "Phiếu nhập được tạo thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    ArrayList<PhieuNhapDTO> ds = PhieuNhapDAO.layDS();
                    formPhieuNhap.updateTableData(ds);
                    
                }
                else
                    JOptionPane.showMessageDialog(this, "Tạo phiếu nhập thất bại thất bại.", "Thông báo", JOptionPane.WARNING_MESSAGE);
                }
        } catch (SQLException ex) {
            Logger.getLogger(Form_ThemPhieuNhap.class.getName()).log(Level.SEVERE, null, ex);
        }
        conn.close();
    }//GEN-LAST:event_btnTaoPhieuNhapActionPerformed

